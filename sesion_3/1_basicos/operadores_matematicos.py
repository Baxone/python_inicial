# matematicos, necesariamente se aplican en numeros.
numero1 = 3
numero2 = 5
# suma
print( 3 + 3 ) # 6
resultado = numero1 + numero2
print(resultado) # 8

# restar 
print(12 - 3) # 9

# multiplicar
print('multiplicar', 12 * 3 ) # 36

# division
print( 12 / 3 ) # 4
print( 13 / 3 ) # 4.33333333333

# modulo - resto 
print( 12 % 3 ) # 0
print( 13 % 3 ) # 1

# division entera 
print( 13 // 3 ) # 4

# potencia
print( 2 ** 3 ) # 2 elevado a 3 => 8
print("--------")
# operador incremento y decremento
numero1 += 1 # 4
print(numero1)
numero1 += 3 # numero1 = numero1 + 3 shortland
print(numero1)
numero1 -= 1 # restar 1
print(numero1)
numero1 -= 3 # restar 3 al numero
print(numero1)

