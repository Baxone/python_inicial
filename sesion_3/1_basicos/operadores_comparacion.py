numero1 = 16
numero2 = 6

# operadores de comparacion -> siempre devuelven un boolean

# mayor que 
print( numero1 > numero2 ) # True
# mayor o igual que
print( numero1 >= numero2 )


# menor que 
print( numero1 < numero2 ) # False
# menor o igual que
print( numero1 <= numero2 ) # False

# Igualdad 
print( 3 == "3" ) # False
print(numero1 == numero2 ) # False
print( 4 == 4 ) # True

# Desigualdad
print( 3 != "3" ) # True
print(numero1 != numero2 ) # True
print(4 != 4 ) # False

